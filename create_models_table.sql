-- Create models table
-- This script creates the models table with all required fields
-- It is idempotent - can be run multiple times safely

-- Step 0: Drop existing objects if they exist (for idempotency)
DROP TABLE IF EXISTS models CASCADE;
DROP FUNCTION IF EXISTS ensure_uppercase_models() CASCADE;

-- Step 1: Create the models table
CREATE TABLE models (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    make VARCHAR(100) NOT NULL,
    model VARCHAR(100) NOT NULL,
    year INTEGER NOT NULL,
    fuel_type VARCHAR(50),
    transmission VARCHAR(50),
    seats INTEGER,
    daily_rate NUMERIC(10, 2),
    features TEXT[],
    description TEXT,
    category_id UUID,
    CONSTRAINT fk_models_category FOREIGN KEY (category_id) REFERENCES vehicle_categories(id) ON DELETE SET NULL
);

-- Step 2: Create indexes
CREATE INDEX IF NOT EXISTS idx_models_make ON models(make);
CREATE INDEX IF NOT EXISTS idx_models_make_model ON models(make, model);
CREATE INDEX IF NOT EXISTS idx_models_year ON models(year);
CREATE INDEX IF NOT EXISTS idx_models_category_id ON models(category_id);

-- Step 3: Create function to ensure make and model are uppercase
CREATE OR REPLACE FUNCTION ensure_uppercase_models()
RETURNS TRIGGER AS $$
BEGIN
    NEW.make := UPPER(NEW.make);
    NEW.model := UPPER(NEW.model);
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Step 4: Create trigger to automatically convert make and model to uppercase
DROP TRIGGER IF EXISTS trigger_uppercase_models ON models;
CREATE TRIGGER trigger_uppercase_models
    BEFORE INSERT OR UPDATE ON models
    FOR EACH ROW
    EXECUTE FUNCTION ensure_uppercase_models();

-- Step 5: Update any existing records to uppercase (if table has data)
UPDATE models 
SET 
    make = UPPER(make),
    model = UPPER(model)
WHERE make != UPPER(make) OR model != UPPER(model);

-- Step 6: Add CHECK constraint to ensure make and model remain uppercase
-- Drop constraints if they exist first
ALTER TABLE models 
DROP CONSTRAINT IF EXISTS chk_models_make_uppercase;

ALTER TABLE models 
DROP CONSTRAINT IF EXISTS chk_models_model_uppercase;

ALTER TABLE models 
ADD CONSTRAINT chk_models_make_uppercase 
CHECK (make = UPPER(make));

ALTER TABLE models 
ADD CONSTRAINT chk_models_model_uppercase 
CHECK (model = UPPER(model));

-- Step 7: Verify the table structure
SELECT 
    column_name,
    data_type,
    is_nullable,
    column_default
FROM information_schema.columns
WHERE table_name = 'models'
ORDER BY ordinal_position;

